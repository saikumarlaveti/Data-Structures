package Time_Complexity;

public class Quaratic {
        public static void main(String[] args) {
            int[] arr = {1,2,3,4,5};
            for(int i = 0;i<arr.length;i++){
                for(int j = 0;j<arr.length;j++){
                    System.out.print(arr[i] + " ");
                }
                System.out.println();
            }
        }
}
